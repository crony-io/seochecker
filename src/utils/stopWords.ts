/**
 * Checks if a word is a stop word.
 */
export function isStopWord(word: string, locale: string = 'en'): boolean {
  const lowerWord = word.toLowerCase();
  if (locale === 'es') {
    return STOP_WORDS_ES.has(lowerWord);
  }
  return STOP_WORDS_EN.has(lowerWord);
}

/**
 * Filters out stop words from an array of words.
 */
export function filterStopWords(words: string[], locale: string = 'en'): string[] {
  return words.filter((word) => !isStopWord(word, locale));
}
